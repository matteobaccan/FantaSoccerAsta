package it.baccan.fantasoccerasta;

/**
 *
 * @author Matteo
 */
public class Calciatore {

    private String codice;
    private String ruolo;
    private String nome;
    private String squadra;
    private String fantamedia;
    private String presenze;
    private String infortunato;
    private String rigorista;
    private String evidenzia;

    /**
     * @return the codice
     */
    public String getCodice() {
        return codice;
    }

    /**
     * @param codice the codice to set
     */
    public void setCodice(String codice) {
        this.codice = codice;
    }

    /**
     * @return the ruolo
     */
    public String getRuolo() {
        return ruolo;
    }

    /**
     * @param ruolo the ruolo to set
     */
    public void setRuolo(String ruolo) {
        this.ruolo = ruolo;
    }

    /**
     * @return the nome
     */
    public String getNome() {
        return nome;
    }

    /**
     * @param nome the nome to set
     */
    public void setNome(String nome) {
        this.nome = nome;
    }

    /**
     * @return the squadra
     */
    public String getSquadra() {
        return squadra;
    }

    /**
     * @param squadra the squadra to set
     */
    public void setSquadra(String squadra) {
        this.squadra = squadra;
    }

    /**
     * @return the fantamedia
     */
    public String getFantamedia() {
        return fantamedia;
    }

    /**
     * @param fantamedia the fantamedia to set
     */
    public void setFantamedia(String fantamedia) {
        this.fantamedia = fantamedia;
    }

    /**
     * @return the presenze
     */
    public String getPresenze() {
        return presenze;
    }

    /**
     * @param presenze the presenze to set
     */
    public void setPresenze(String presenze) {
        this.presenze = presenze;
    }

    /**
     * @return the infortunato
     */
    public String getInfortunato() {
        return infortunato;
    }

    /**
     * @param infortunato the infortunato to set
     */
    public void setInfortunato(String infortunato) {
        this.infortunato = infortunato;
    }

    /**
     * @return the rigorista
     */
    public String getRigorista() {
        return rigorista;
    }

    /**
     * @param rigorista the rigorista to set
     */
    public void setRigorista(String rigorista) {
        this.rigorista = rigorista;
    }

    /**
     * @return the evidenzia
     */
    public String getEvidenzia() {
        return evidenzia;
    }

    /**
     * @param evidenzia the evidenzia to set
     */
    public void setEvidenzia(String evidenzia) {
        this.evidenzia = evidenzia;
    }

}
